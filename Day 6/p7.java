class Demo {
    public static void main(String[] args) {
        int rows = 3; // Number of rows

        for (int i = 0; i < rows; i++) {
            for (char ch = 'A'; ch <= 'D'; ch++) {
                System.out.print(ch + " ");
            }
            System.out.println();
        }
    }
}




/*OUTPUT:-

A B C D
A B C D
A B C D     */
