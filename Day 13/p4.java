/*Program 4: Write a java program to calculate the factorial
 of a given number.
 Input: 5
 Output:
 The Factorial of 5 is: 120											*/



class Demo{
	public static void main(String [] args){
		int a = 1;
		for(int i=1; i<=5;i++){
			a = a*i;
		}
		System.out.println("The Factorial of 5 is: "+a);
	}
}



/*OUTPUT:- The Factorial of 5 is: 120								*/